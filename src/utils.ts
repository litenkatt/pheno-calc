export const gdl_to_gL = (gdl: number) => gdl * 10; // Albumin  g/dL  ➜ g/L
export const mgdl_to_umolL = (mgdl: number) => mgdl * 88.4; // Kreatinin mg/dL ➜ µmol/L
export const mgdl_to_mmolL_gluc = (mgdl: number) => mgdl * 0.0555; // Glukos    mg/dL ➜ mmol/L
export const mgL_to_mgdl = (mgL: number) => mgL * 0.1; // CRP       mg/L  ➜ mg/dL
export const ukatL_to_UL = (ukat: number) => ukat * 60; // ALP       µkat/L ➜ U/L
